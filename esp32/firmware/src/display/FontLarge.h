// Map ASCII char to glyph index, returns -1 if not in font
static inline int8_t largeFontIndex(char c) {
    if (c == ' ') return 0;
    if (c >= '0' && c <= '9') return 1 + (c - '0');
    switch (c) {
        case '.': return 11;
        case ',': return 12;
        case '$': return 13;
        case '%': return 14;
        case ':': return 15;
        case '-': return 16;
        case '+': return 17;
        default:  return -1;
    }
}

